SELECT

  SE7.E7_ANO,
  SE7.E7_NATUREZ,
  SED.ED_DESCRIC,
  SE7.E7_VALJAN1,
  SE7.E7_VALFEV1,
  SE7.E7_VALMAR1,
  SE7.E7_VALABR1,
  SE7.E7_VALMAI1,
  SE7.E7_VALJUN1,
  SE7.E7_VALJUL1,
  SE7.E7_VALAGO1,
  SE7.E7_VALSET1,
  SE7.E7_VALOUT1,
  SE7.E7_VALNOV1,
  SE7.E7_VALDEZ1

FROM SE7010 SE7

LEFT JOIN SED010 SED
  ON SE7.E7_FILIAL = SED.ED_FILIAL
  AND SE7.E7_NATUREZ = SED.ED_CODIGO

WHERE SE7.D_E_L_E_T_ = ''
AND SED.D_E_L_E_T_ = ''
AND SE7.E7_FILIAL = '01'
AND SE7.E7_ANO = '2018'