SELECT
  SUBSTRING(SE5.E5_DATA, 1, 6) E5_DATA,
  SE5.E5_NATUREZ,
  SED.ED_DESCRIC,
  SED.ED_XINGDES,
  SE5.E5_RECPAG,
  SE5.E5_VALOR,
  SINAL = (CASE
    WHEN (SED.ED_XINGDES = '1' AND
      SE5.E5_RECPAG = 'R') OR
      (SED.ED_XINGDES = '2' AND
      SE5.E5_RECPAG = 'P') THEN 1
    ELSE -1
  END)
FROM SE5010 SE5
LEFT JOIN SED010 SED
  ON SE5.E5_NATUREZ = SED.ED_CODIGO
WHERE SE5.E5_FILIAL = '01'
AND SED.D_E_L_E_T_ = ''
AND SE5.D_E_L_E_T_ = ''
AND SE5.E5_SITUACA NOT IN ('C')
AND SE5.E5_TIPODOC IN ('VL')
AND SE5.E5_NATUREZ BETWEEN '5.1.1.1   ' AND '5.1.9.2.1 '
AND SE5.E5_DATA BETWEEN '20180101' AND '20181230'
AND SE5.E5_BANCO + SE5.E5_AGENCIA + SE5.E5_CONTA IN ('0011897 590562    ', '0011897 5905621   ', '1042873 191       ', '1042873 191-8     ', '1042873 1914      ', '1042873 1918      ', '1042873 194       ', '1042873 1942      ', '1042873 62        ', '1043278 0         ', '2370301 5461      ', '3410057 63501     ', 'CX1000010000000001', 'FFC0000100001     ')



